def main() -> int:
    corr, counts, n_days = load_matrix()
    order, matrix = to_chord_matrix(corr)
    as_of = pd.Timestamp.now().strftime("%Y-%m-%d")

    print(f"[OK] {len(order)} sectors, {n_days} trading days")
    print(corr.loc[order, order].round(2).to_string())
    off_diag = [v for i, row in enumerate(matrix) for j, v in enumerate(row) if i != j]
    print(f"\ncorr range (off-diagonal, clipped at 0): "
          f"{min(off_diag):.2f} .. {max(off_diag):.2f}")

    OUT.write_text(build_html(order, matrix, counts, n_days, as_of), encoding="utf-8")
    print(f"\n[OK] wrote {OUT.relative_to(ROOT)} ({OUT.stat().st_size/1024:.0f} KB)")
    print("     [WARN] co-movement, NOT dependency - caveat rendered on the page.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
