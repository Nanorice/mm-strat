def _render_filter_sensitivity(filter_data: list):
    """Render the RS threshold yield curve."""
    st.markdown("### Filter Sensitivity (RS Threshold)")
    st.markdown(
        "The **Yield Curve** shows how tightening the RS filter affects candidate count vs win rate. "
        "Look for the *knee* where win rate improves without losing too many candidates."
    )

    df = pd.DataFrame(filter_data)

    # Dual-axis chart
    fig = go.Figure()

    # Candidate count (bars)
    fig.add_trace(go.Bar(
        x=df['threshold'],
        y=df['candidate_count'],
        name='Candidates',
        yaxis='y',
        marker_color='rgba(55, 83, 109, 0.7)',
        text=df['candidate_count'],
        textposition='outside'
    ))

    # Win rate (line)
    fig.add_trace(go.Scatter(
        x=df['threshold'],
        y=df['win_rate'],
        name='Win Rate %',
        yaxis='y2',
        mode='lines+markers',
        line=dict(color='#2ca02c', width=3),
        marker=dict(size=10)
    ))

    # Average return (line)
    fig.add_trace(go.Scatter(
        x=df['threshold'],
        y=df['avg_return'],
        name='Avg Return %',
        yaxis='y2',
        mode='lines+markers',
        line=dict(color='#d62728', width=2, dash='dash'),
        marker=dict(size=8)
    ))

    fig.update_layout(
        title='RS Threshold Sensitivity Analysis',
        xaxis=dict(title='RS Rating Threshold', tickmode='array', tickvals=df['threshold']),
        yaxis=dict(title='Candidate Count', side='left'),
        yaxis2=dict(title='Win Rate / Avg Return %', overlaying='y', side='right'),
        legend=dict(orientation='h', yanchor='bottom', y=1.02),
        height=400
    )

    st.plotly_chart(fig, use_container_width=True)

    # Data table
    with st.expander("View Raw Data"):
        st.dataframe(df, use_container_width=True, hide_index=True)


