def _render_feature_leaderboard(eda_data: dict):
    """Render feature leaderboard."""
    st.markdown("### Feature Leaderboard")
    st.markdown("Features ranked by composite score (40% IC + 30% Stability + 30% KS)")

    feature_scores = eda_data.get('feature_scores', [])
    if feature_scores:
        df = pd.DataFrame(feature_scores)

        # Filter controls
        col1, col2 = st.columns([1, 3])
        with col1:
            show_filter = st.selectbox(
                "Show:",
                ["All", "Passed Only", "Failed Only"],
                key='d2_filter'
            )

        if show_filter == "Passed Only":
            df = df[df['passed'] == True]
        elif show_filter == "Failed Only":
            df = df[df['passed'] == False]

        # Format and display
        display_df = df[['feature', 'composite', 'ic_score', 'stability_score', 'ks_score', 'signal_type', 'passed']].copy()
        display_df.columns = ['Feature', 'Composite', 'IC', 'Stability', 'KS', 'Signal Type', 'Passed']

        # Color code
        def highlight_passed(row):
            if row['Passed']:
                return ['background-color: #d4edda'] * len(row)
            return ['background-color: #f8d7da'] * len(row)

        st.dataframe(
            display_df.style.apply(highlight_passed, axis=1).format({
                'Composite': '{:.3f}',
                'IC': '{:.3f}',
                'Stability': '{:.3f}',
                'KS': '{:.3f}'
            }),
            use_container_width=True,
            height=500
        )
    else:
        st.info("No feature scores available")


def _render_ks_distributions(eda_data: dict):
    """Render KS distribution comparisons."""
    st.markdown("### KS Distribution Comparison")
    st.markdown("Compare feature distributions between Q1 (losers) and Q4 (winners)")

    ks_distributions = eda_data.get('ks_distributions', {})
    if ks_distributions:
        features = list(ks_distributions.keys())
        selected_feature = st.selectbox("Select Feature:", features, key='d2_ks_feature')

        if selected_feature and selected_feature in ks_distributions:
            dist_data = ks_distributions[selected_feature]
            q1_values = dist_data.get('q1', [])
            q4_values = dist_data.get('q4', [])

            if q1_values and q4_values:
                fig = go.Figure()

                fig.add_trace(go.Histogram(
                    x=q1_values,
                    name='Q1 (Losers)',
                    marker_color='rgba(239, 83, 80, 0.6)',
                    nbinsx=30
                ))

                fig.add_trace(go.Histogram(
                    x=q4_values,
                    name='Q4 (Winners)',
                    marker_color='rgba(38, 166, 154, 0.6)',
                    nbinsx=30
                ))

                fig.add_vline(
                    x=dist_data.get('q1_median', 0),
                    line_dash="dash",
                    line_color="red",
                    annotation_text=f"Q1 Median: {dist_data.get('q1_median', 0):.2f}"
                )
                fig.add_vline(
                    x=dist_data.get('q4_median', 0),
                    line_dash="dash",
                    line_color="green",
                    annotation_text=f"Q4 Median: {dist_data.get('q4_median', 0):.2f}"
                )

                fig.update_layout(
                    title=f"KS Distribution: {selected_feature}",
                    barmode='overlay',
                    xaxis_title=selected_feature,
                    yaxis_title='Count',
                    template='plotly_white',
                    height=400
                )

                st.plotly_chart(fig, use_container_width=True)

                q1_med = dist_data.get('q1_median', 0)
                q4_med = dist_data.get('q4_median', 0)
                separation = abs(q4_med - q1_med)
                st.metric("Median Separation", f"{separation:.3f}")
    else:
        st.info("KS distribution data not available.")


