def load_matrix() -> tuple[pd.DataFrame, pd.Series, int]:
    """252d sector-return correlation + per-sector company counts.

    Equal-weight daily sector return from price_data; ETF pseudo-sectors excluded
    (company_profiles tags 16 ETF:* buckets that aren't real sectors)."""
    con = db.connect(str(DB_PATH), read_only=True)
    try:
        rets = con.execute("""
            WITH universe AS (
                SELECT ticker, sector FROM company_profiles
                WHERE sector IS NOT NULL AND sector NOT LIKE 'ETF:%'
            ),
            px AS (
                SELECT p.date, u.sector, p.close,
                       LAG(p.close) OVER (PARTITION BY p.ticker ORDER BY p.date) prev
                FROM price_data p JOIN universe u USING (ticker)
                WHERE p.date >= (SELECT MAX(date) - INTERVAL 400 DAY FROM price_data)
            )
            SELECT date, sector, AVG(close / prev - 1) AS ret
            FROM px WHERE prev IS NOT NULL AND prev > 0
            GROUP BY date, sector
        """).fetchdf()
        counts = con.execute("""
            SELECT sector, COUNT(*) n FROM company_profiles
            WHERE sector IS NOT NULL AND sector NOT LIKE 'ETF:%'
            GROUP BY sector
        """).fetchdf().set_index("sector")["n"]
    finally:
        con.close()

    wide = rets.pivot(index="date", columns="sector", values="ret")
    wide = wide.dropna(how="any").tail(WINDOW)
    return wide.corr(), counts, len(wide)


def to_chord_matrix(corr: pd.DataFrame) -> tuple[list[str], list[list[float]]]:
    """Chord needs a non-negative weight matrix with a zero diagonal.

    Self-correlation (the 1.0 diagonal) would swamp every ribbon, and a negative
    correlation has no chord representation — clipped at 0 and captioned.
    """
    order = sorted(corr.columns, key=lambda s: -corr[s].drop(s).mean())
    m = corr.loc[order, order].clip(lower=0.0)
    for s in order:
        m.loc[s, s] = 0.0
    return order, m.round(4).values.tolist()


