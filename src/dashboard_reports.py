# PAGE 1: D1 ANALYSIS (Load pre-generated stats)
# =============================================================================

def render_d1_analysis():
    """Render D1 Analysis page - Trade Physics (from pre-generated report)."""
    from src.viz_library import (
        create_mae_mfe_scatter,
        create_e_ratio_histogram,
        create_time_to_peak_histogram
    )

    st.title("📊 D1 Analysis: Trade Physics")
    st.markdown("Trade characteristics: MAE/MFE, E-Ratio, crash rates.")

    # Try to load pre-generated report
    d1_report = load_d1_report()

    if d1_report is None:
        st.warning("D1 Analysis report not generated yet.")
        st.markdown("""
        **To generate:**
        ```bash
        python model.py m01 --steps train --report
        ```

        This will pre-compute trade physics and save to `models/d1_analysis.json`.
        """)

        # Fallback: show what the report WILL contain
        st.info("Once generated, this page will show:")
        col1, col2, col3, col4 = st.columns(4)
        with col1:
            st.metric("Total Trades", "N/A")
        with col2:
            st.metric("Median MFE", "N/A")
        with col3:
            st.metric("Median MAE", "N/A")
        with col4:
            st.metric("Crash Rate", "N/A")
        return

    # Display pre-computed metrics
    col1, col2, col3, col4 = st.columns(4)
    with col1:
        st.metric("Total Trades", f"{d1_report.get('total_trades', 0):,}")
    with col2:
        st.metric("Median MFE", f"{d1_report.get('median_mfe', 0):.1f}%")
    with col3:
        st.metric("Median MAE", f"{d1_report.get('median_mae', 0):.1f}%")
    with col4:
        st.metric("Median E-Ratio", f"{d1_report.get('median_e_ratio', 0):.2f}")

    # Crash rate from report
    st.markdown("### Crash Rate Analysis")
    crash_rate = d1_report.get('crash_rate', 0)
    survived_rate = 100 - crash_rate

    col1, col2 = st.columns(2)
    with col1:
        st.metric("Crashed", f"{crash_rate:.1f}%", delta=None)
    with col2:
        st.metric("Survived", f"{survived_rate:.1f}%", delta=None)

    st.info(f"Using stop multiplier: {d1_report.get('stop_multiplier', 2.0)}x nATR")

    # NEW: MAE/MFE Scatter Plot
    if 'mae_mfe_scatter' in d1_report and len(d1_report['mae_mfe_scatter']) > 0:
        st.markdown("---")
        st.markdown("### MAE/MFE Analysis")
        st.markdown("Maximum Adverse/Favorable Excursion scatter with E-Ratio reference lines. "
                   "Trades above the **E-Ratio 3.0** line indicate strong edge.")

        try:
            fig = create_mae_mfe_scatter(d1_report['mae_mfe_scatter'])
            st.plotly_chart(fig, use_container_width=True)
        except Exception as e:
            st.error(f"Error rendering MAE/MFE scatter: {e}")

    # NEW: E-Ratio Distribution
    if 'e_ratio_distribution' in d1_report and len(d1_report['e_ratio_distribution']) > 0:
        st.markdown("---")
        st.markdown("### E-Ratio Distribution")
        pct_above = d1_report.get('e_ratio_gt_3_pct', 0)
        st.markdown(f"E-Ratio = MFE / |MAE| measures upside/downside asymmetry. "
                   f"**{pct_above:.1f}%** of trades exceed the 3.0 benchmark.")

        try:
            fig = create_e_ratio_histogram(d1_report['e_ratio_distribution'], benchmark=3.0)
            st.plotly_chart(fig, use_container_width=True)
        except Exception as e:
            st.error(f"Error rendering E-Ratio histogram: {e}")

    # NEW: Time to Peak
    if 'time_to_peak' in d1_report and len(d1_report['time_to_peak']) > 0:
        st.markdown("---")
        st.markdown("### Time to Peak (MFE)")
        median_days = pd.Series(d1_report['time_to_peak']).median()
        st.markdown(f"Days to reach Maximum Favorable Excursion. "
                   f"Median: **{median_days:.0f} days**. This informs optimal holding periods.")

        try:
            fig = create_time_to_peak_histogram(d1_report['time_to_peak'])
            st.plotly_chart(fig, use_container_width=True)
        except Exception as e:
            st.error(f"Error rendering time-to-peak histogram: {e}")


