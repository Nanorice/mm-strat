def render_m02_report():
    """Render M02 Report page - loads pre-generated model config."""
    st.title("🎯 M02 Report: Ignition Classifier")
    st.markdown("Triple barrier classification results.")
    
    config = load_model_config('m02')
    d3_summary = load_d3_summary()
    
    # D3 Summary (pre-computed)
    if d3_summary:
        st.markdown("### Triple Barrier Outcomes")
        
        col1, col2, col3 = st.columns(3)
        with col1:
            tp = d3_summary.get('tp_rate', 0)
            st.metric("Take Profit", f"{tp:.1f}%")
        with col2:
            sl = d3_summary.get('sl_rate', 0)
            st.metric("Stop Loss", f"{sl:.1f}%")
        with col3:
            time_exit = d3_summary.get('time_rate', 0)
            st.metric("Time Exit", f"{time_exit:.1f}%")
        
        # Pie chart
        fig = px.pie(
            values=[tp, sl, time_exit],
            names=['TP', 'SL', 'Time'],
            title='Barrier Outcome Distribution',
            color_discrete_sequence=['green', 'red', 'gray']
        )
        fig.update_layout(height=350)
        st.plotly_chart(fig, use_container_width=True)
    else:
        st.info("D3 summary not found. Run `python model.py m02 --steps label` first.")
    
    # Model metrics from config
    if config:
        st.markdown("### Model Validation Results")
        
        metrics = config.get('validation_metrics', [])
        if metrics:
            metrics_df = pd.DataFrame(metrics)
            
            if 'auc' in metrics_df.columns:
                avg_auc = metrics_df['auc'].mean()
                st.metric("Average AUC", f"{avg_auc:.3f}")
            
            st.dataframe(metrics_df, use_container_width=True, hide_index=True)
    else:
        st.warning("M02 model not trained. Run `python model.py m02 --steps train` first.")


