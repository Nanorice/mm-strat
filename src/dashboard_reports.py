"""
Dashboard ML Reports Module
===========================

Provides report page rendering functions for the TradeOps Dashboard.

Pages load PRE-GENERATED report files (JSON/CSV) from models/ directory.
The reports are generated by the CLI (python model.py m01 --report).

This is fast because the dashboard only loads small JSON files,
not raw parquet data.
"""

import streamlit as st
import pandas as pd
import numpy as np
import plotly.graph_objects as go
import plotly.express as px
from pathlib import Path
import json
import glob
from datetime import datetime


# =============================================================================
# DATA LOADING HELPERS (Load pre-generated reports, not raw data)
# =============================================================================

@st.cache_data(ttl=60)
def load_model_config(model_name: str):
    """Load model configuration JSON (contains validation metrics)."""
    path = Path(f'models/{model_name.lower()}_config.json')
    if path.exists():
        with open(path, 'r') as f:
            return json.load(f)
    return None


@st.cache_data(ttl=60)
def load_feature_importance(model_name: str):
    """Load feature importance CSV."""
    path = Path(f'models/feature_importance_{model_name.lower()}.csv')
    if path.exists():
        return pd.read_csv(path)
    return None


@st.cache_data(ttl=60)
def load_latest_report(model_name: str):
    """Load the latest markdown report for a model."""
    pattern = f'models/model_report_{model_name.upper()}_*.md'
    files = sorted(glob.glob(pattern), reverse=True)
    if files:
        with open(files[0], 'r', encoding='utf-8') as f:
            return f.read(), files[0]
    return None, None


@st.cache_data(ttl=60)
def load_d1_report():
    """Load D1 analysis report (trade physics pre-computed stats)."""
    path = Path('models/d1_analysis.json')
    if path.exists():
        with open(path, 'r') as f:
            return json.load(f)
    return None


@st.cache_data(ttl=60)
def load_d3_summary():
    """Load D3 triple barrier summary (pre-computed stats)."""
    # Check both possible paths
    paths = [
        Path('data/ml/d3_summary.json'),
        Path('models/d3_summary.json')
    ]
    for path in paths:
        if path.exists():
            with open(path, 'r') as f:
                return json.load(f)
    return None


