def render_dual_model():
    """Render Dual-Model page - Combined M01+M02 analysis."""
    st.title("🔀 Dual-Model Analysis")
    st.markdown("Compare M01 (regression) and M02 (classification) model outputs.")
    
    m01_config = load_model_config('m01')
    m02_config = load_model_config('m02')
    
    col1, col2 = st.columns(2)
    
    with col1:
        st.markdown("### M01 (Regressor)")
        if m01_config:
            metrics = m01_config.get('validation_metrics', [])
            if metrics:
                avg_edge = np.mean([m['selection_edge'] for m in metrics])
                st.metric("Selection Edge", f"{avg_edge:+.2f}%")
                st.metric("Features", len(m01_config.get('feature_columns', [])))
        else:
            st.info("M01 not trained")
    
    with col2:
        st.markdown("### M02 (Classifier)")
        if m02_config:
            metrics = m02_config.get('validation_metrics', [])
            if metrics and 'auc' in metrics[0]:
                avg_auc = np.mean([m['auc'] for m in metrics])
                st.metric("Average AUC", f"{avg_auc:.3f}")
            st.metric("Features", len(m02_config.get('feature_columns', [])))
        else:
            st.info("M02 not trained")
    
    st.markdown("---")
    st.markdown("""
    ### Combined Scoring Strategy
    
    **Dual-Model Approach:**
    1. M01 predicts expected return %
    2. M02 predicts ignition probability
    3. Combined rank uses both signals
    
    **Filter:** M01 Rank ≤ 3 AND M02 Prob > 0.5
    """)


