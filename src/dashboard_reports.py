def _render_sepa_decile_boxes(eda_data: dict):
    """Render ALL decile box plots in a grid layout (no feature selection needed).

    Performance optimization: Renders plots in batches to avoid browser overload.
    """
    st.markdown("### Decile Monotonicity Analysis")
    st.markdown("**Box plots** show return distribution per decile - revealing variance hidden by mean charts")

    decile_box_data = eda_data.get('decile_box_data', {})

    if not decile_box_data:
        st.info("Decile box plot data not available. Re-run EDA workflow to generate.")
        return

    features = list(decile_box_data.keys())
    st.caption(f"Total: {len(features)} features with decile analysis")

    # Add a "Show Top N" slider to reduce initial load time (only if >6 features)
    if len(features) > 6:
        max_features = min(len(features), 20)
        n_features = st.slider(
            "Number of features to display",
            min_value=4,
            max_value=len(features),
            value=max_features,
            step=2,
            help="Showing fewer features improves page load speed"
        )
        features_to_show = features[:n_features]
    else:
        # For small datasets, show all features without slider
        features_to_show = features

    # Render features in grid layout (2 columns)
    for i in range(0, len(features_to_show), 2):
        col1, col2 = st.columns(2)

        with col1:
            if i < len(features_to_show):
                _render_single_decile_box(features_to_show[i], decile_box_data[features_to_show[i]])

        with col2:
            if i + 1 < len(features_to_show):
                _render_single_decile_box(features_to_show[i + 1], decile_box_data[features_to_show[i + 1]])


def _render_single_decile_box(feature_name: str, box_arrays: dict):
    """Render a single decile box plot with optimized visual clarity.

    Fixes:
    - Points side-by-side (pointpos=-1.8) instead of overlaid
    - Reduced jitter (0.2) for tighter scatter
    - Strict outlier-only mode for datasets >1000 samples
    - Box lines render on top with higher opacity
    """
    fig = go.Figure()

    for decile_name in sorted(box_arrays.keys(), key=lambda x: int(x[1:])):
        returns = box_arrays[decile_name]
        decile_num = int(decile_name[1:])
        n_samples = len(returns)

        # Adaptive point rendering: outliers only if >1000 samples
        if n_samples > 1000:
            boxpoints_mode = 'outliers'
        else:
            boxpoints_mode = 'outliers'  # Always use outliers to avoid browser overload

        fig.add_trace(go.Box(
            y=returns,
            name=decile_name,
            boxpoints=boxpoints_mode,
            jitter=0.2,  # Tighter scatter (was 0.3)
            pointpos=-1.8,  # Shift points to the left
            marker=dict(
                color='#2ca02c' if decile_num == 10 else '#1f77b4',
                size=2,
                opacity=0.3,  # Lower opacity for points
                line=dict(width=0)
            ),
            line=dict(color='black', width=2),  # Thicker box lines for visibility
            fillcolor='rgba(255,255,255,0.8)',  # Semi-transparent box fill
            boxmean=True,
            opacity=0.9  # Box geometry on top
        ))

    fig.add_hline(y=0, line_dash="dash", line_color="red", line_width=2)

    fig.update_layout(
        title=f"<b>{feature_name}</b>",
        xaxis_title="Decile",
        yaxis_title="Return %",
        template='plotly_white',
        height=400,
        showlegend=False,
        margin=dict(l=40, r=40, t=40, b=40),
        yaxis=dict(
            range=[-30, 100],
            zeroline=True,
            zerolinewidth=2,
            zerolinecolor='red'
        )
    )

    st.plotly_chart(fig, use_container_width=True)

    # Summary metrics (compact)
    d1_returns = box_arrays.get('D1', [])
    d10_returns = box_arrays.get('D10', [])

    if d1_returns and d10_returns:
        spread = np.median(d10_returns) - np.median(d1_returns)
        st.caption(
            f"D1: {np.median(d1_returns):.1f}% | D10: {np.median(d10_returns):.1f}% | "
            f"Spread: {spread:+.1f}% | D10 Max: {np.max(d10_returns):.0f}%"
        )


def _render_sepa_industry(eda_data: dict):
    """Render per-industry box plots."""
    st.markdown("### Industry Performance Analysis")
    st.markdown("Return distribution by industry - categorical analysis (not monotonicity)")

    industry_box_data = eda_data.get('industry_box_data', [])

    if not industry_box_data:
        st.info("Industry box plot data not available. Re-run EDA workflow to generate.")
        return

    # Create industry box plot using RAW DATA
    fig = go.Figure()

    for ind in industry_box_data[:15]:  # Top 15 industries
        returns = ind['returns']
        fig.add_trace(go.Box(
            y=returns,
            name=ind['industry'][:20],  # Truncate long names
            boxpoints='outliers',
            marker=dict(
                color='#1f77b4',
                size=2,
                opacity=0.5
            ),
            line=dict(color='black', width=1),
            boxmean=True
        ))

    fig.add_hline(y=0, line_dash="dash", line_color="gray", opacity=0.5)

    fig.update_layout(
        title="Return Distribution by Industry (Top 15 by Frequency)",
        xaxis_title='Industry',
        yaxis_title='Return (%)',
        template='plotly_white',
        height=500,
        showlegend=False,
        xaxis_tickangle=-45
    )

    st.plotly_chart(fig, use_container_width=True)

    # Summary table
    st.markdown("#### Industry Statistics")
    summary_data = []
    for ind in industry_box_data:
        returns = ind['returns']
        summary_data.append({
            'Industry': ind['industry'],
            'Trades': ind['count'],
            'Median %': np.median(returns),
            'Mean %': np.mean(returns),
            'Win Rate %': (np.array(returns) > 0).mean() * 100,
            '>50% Return %': (np.array(returns) > 50).mean() * 100,
            'Std Dev': np.std(returns)
        })

    ind_df = pd.DataFrame(summary_data)
    st.dataframe(
        ind_df.style.format({
            'Median %': '{:.2f}',
            'Mean %': '{:.2f}',
            'Win Rate %': '{:.1f}',
            '>50% Return %': '{:.1f}',
            'Std Dev': '{:.1f}'
        }),
        use_container_width=True,
        hide_index=True
    )


def _render_sepa_super_performers(eda_data: dict):
    """Render super-performer histogram (all 10 deciles)."""
    st.markdown("### Super-Performer Analysis")
    st.markdown("**The Home-Run Hunter**: What are the odds of catching a 100%+ winner?")

    super_perf = eda_data.get('super_performer_analysis', {})

    if not super_perf:
        st.info("Super-performer analysis not available. Re-run EDA workflow to generate.")
        return

    # Show summary metrics for key deciles
    col1, col2, col3 = st.columns(3)

    decile_configs = [
        ('decile_1', 'D1 (Lowest RS)', col1),
        ('decile_5', 'D5 (Middle RS)', col2),
        ('decile_10', 'D10 (Highest RS)', col3)
    ]

    for decile_key, title, col in decile_configs:
        if decile_key not in super_perf:
            continue

        data = super_perf[decile_key]
        with col:
            st.markdown(f"#### {title}")
            st.metric("Home Runs (>100%)", f"{data['n_home_runs']} ({data['pct_home_runs']:.1f}%)")
            st.metric("Super-Performers (>50%)", f"{data['n_super_performers']} ({data['pct_super_performers']:.1f}%)")
            st.metric("Median Return", f"{data['median_return']:.1f}%")

    st.markdown("---")

    # Histogram comparison - ALL 10 DECILES
    st.markdown("#### Return Distribution by RS Decile (All 10)")

    fig = go.Figure()

    # Color gradient from red (D1) to green (D10)
    colors = px.colors.sample_colorscale("RdYlGn", [i/9 for i in range(10)])

    for decile_num in range(1, 11):
        decile_key = f'decile_{decile_num}'
        if decile_key not in super_perf:
            continue

        data = super_perf[decile_key]
        fig.add_trace(go.Bar(
            name=f'D{decile_num}',
            x=data['return_bins'],
            y=data['counts'],
            marker_color=colors[decile_num - 1],
            text=data['counts'],
            textposition='outside',
            hovertemplate=(
                f"<b>Decile {decile_num}</b><br>"
                f"Bin: %{{x}}<br>"
                f"Count: %{{y}}<br>"
                f"Home Runs: {data['n_home_runs']} ({data['pct_home_runs']:.1f}%)<extra></extra>"
            )
        ))

    fig.update_layout(
        title="Trade Count by Return Bucket (Log Scale)",
        xaxis_title='Return Range',
        yaxis_title='Number of Trades',
        yaxis_type='log',
        barmode='group',
        template='plotly_white',
        height=500,
        legend=dict(
            orientation='v',
            yanchor='top',
            y=1,
            xanchor='right',
            x=1.15
        )
    )

    st.plotly_chart(fig, use_container_width=True)

    # Key insight
    if 'decile_10' in super_perf and 'decile_1' in super_perf:
        d10_hr = super_perf['decile_10']['pct_home_runs']
        d1_hr = super_perf['decile_1']['pct_home_runs']
        if d10_hr > 0 and d1_hr == 0:
            st.success(
                f"D10 has {d10_hr:.1f}% home runs vs D1's {d1_hr:.1f}%. "
                f"**Relative Strength is the gateway to Super-Performers.**"
            )
        elif d10_hr > d1_hr:
            st.info(f"D10 has {d10_hr:.1f}% home runs vs D1's {d1_hr:.1f}%")


