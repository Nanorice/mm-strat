"""
Report generation for model training results.
==============================================

Generates comprehensive markdown scorecards with:
- Executive summary with viability assessment
- Ranking quality metrics (IC, Lift, Precision@K)
- Volatility detector test
- Walk-forward validation results
- Error analysis (FOMO vs Toxic)
- Feature importance
"""

import logging
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional

import pandas as pd

logger = logging.getLogger("ReportGenerator")


class ReportGenerator:
    """Generate comprehensive markdown scorecards."""
    
    def __init__(self, output_dir: Path):
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)
    
    def generate_scorecard(
        self,
        model_name: str,
        target_type: str,
        metrics_summary: Dict,
        fold_metrics: pd.DataFrame,
        feature_importance: pd.DataFrame = None,
        error_analysis: Dict = None,
        vol_correlation: Dict = None
    ) -> str:
        """
        Generate complete scorecard report.
        
        Sections:
        1. Executive Summary (viability, key metrics)
        2. Ranking Quality (IC, Lift, Precision@K, Recall@K)
        3. Volatility Detector Test
        4. Walk-Forward Results Table
        5. Error Analysis (FOMO vs Toxic)
        6. Feature Importance
        
        Args:
            model_name: Name of the model (e.g., 'M01')
            target_type: Type of target used (e.g., 'return_pct', 'survivor_mfe')
            metrics_summary: Aggregated metrics from evaluator
            fold_metrics: Per-fold metrics DataFrame
            feature_importance: Feature importance DataFrame
            error_analysis: FOMO/Toxic analysis dict
            vol_correlation: Volatility correlation dict
            
        Returns:
            Path to saved report
        """
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        report_path = self.output_dir / f"model_report_{model_name}_{timestamp}.md"
        
        lines = []
        
        # Header
        lines.append(f"# {model_name} Scorecard Report")
        lines.append("")
        lines.append(f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        lines.append(f"**Target Type:** {target_type}")
        lines.append("")
        lines.append("---")
        lines.append("")
        
        # Executive Summary
        lines.extend(self._generate_executive_summary(metrics_summary, fold_metrics))
        
        # Ranking Quality
        lines.extend(self._generate_ranking_quality(metrics_summary))
        
        # Volatility Detector Test
        if vol_correlation:
            lines.extend(self._generate_vol_detector_section(vol_correlation))
        
        # Walk-Forward Results
        lines.extend(self._generate_fold_table(fold_metrics))
        
        # Error Analysis
        if error_analysis:
            lines.extend(self._generate_error_analysis(error_analysis))
        
        # Feature Importance
        if feature_importance is not None and len(feature_importance) > 0:
            lines.extend(self._generate_feature_importance(feature_importance))
        
        # Footer
        lines.append("---")
        lines.append("")
        lines.append(f"*Report generated by {model_name} Evaluator*")
        
        # Write report
        with open(report_path, 'w', encoding='utf-8') as f:
            f.write('\n'.join(lines))
        
        logger.info(f"Saved scorecard to {report_path}")
        return str(report_path)
    
    def _generate_executive_summary(
        self, 
        metrics: Dict, 
        fold_metrics: pd.DataFrame
    ) -> List[str]:
        """Generate executive summary section."""
        lines = []
        lines.append("## Executive Summary")
        lines.append("")
        
        # Viability assessment
        avg_ic = metrics.get('avg_ic', 0)
        avg_edge = metrics.get('avg_selection_edge', 0)
        ic_sharpe = metrics.get('ic_sharpe', 0)
        
        if avg_ic > 0.10 and avg_edge > 1.5 and ic_sharpe > 1.0:
            viability = "✅ **VIABLE** - Strong ranking signal"
        elif avg_ic > 0.05 and avg_edge > 0.5:
            viability = "⚠️ **MARGINAL** - Weak but usable signal"
        else:
            viability = "❌ **NOT VIABLE** - Insufficient signal"
        
        lines.append(f"**Trading Viability:** {viability}")
        lines.append("")
        
        # Key metrics table
        lines.append("### Key Metrics")
        lines.append("")
        lines.append("| Metric | Value | Threshold | Status |")
        lines.append("|--------|-------|-----------|--------|")
        
        # IC
        ic_status = "✅ PASS" if avg_ic > 0.10 else ("⚠️ WEAK" if avg_ic > 0.05 else "❌ FAIL")
        lines.append(f"| IC (Spearman) | {avg_ic:.3f} | > 0.10 | {ic_status} |")
        
        # IC Sharpe
        ic_sharpe_status = "✅ PASS" if ic_sharpe > 1.0 else ("⚠️ WEAK" if ic_sharpe > 0.5 else "❌ FAIL")
        lines.append(f"| IC Sharpe | {ic_sharpe:.2f} | > 1.0 | {ic_sharpe_status} |")
        
        # Selection Edge
        edge_status = "✅ PASS" if avg_edge > 1.5 else ("⚠️ WEAK" if avg_edge > 0.5 else "❌ FAIL")
        lines.append(f"| Selection Edge | {avg_edge:+.2f}% | > 1.5% | {edge_status} |")
        
        # Edge Sharpe
        edge_sharpe = metrics.get('edge_sharpe', 0)
        edge_sharpe_status = "✅ PASS" if edge_sharpe > 0.5 else "❌ FAIL"
        lines.append(f"| Edge Sharpe | {edge_sharpe:.2f} | > 0.5 | {edge_sharpe_status} |")
        
        # IC Consistency
        ic_consistency = metrics.get('ic_consistency', 0)
        lines.append(f"| IC Consistency | {ic_consistency:.0%} | > 70% | {'✅' if ic_consistency > 0.7 else '⚠️'} |")
        
        lines.append("")
        lines.append("---")
        lines.append("")
        
        return lines
    
    def _generate_ranking_quality(self, metrics: Dict) -> List[str]:
        """Generate ranking quality analysis section."""
        lines = []
        lines.append("## Ranking Quality Analysis")
        lines.append("")
        
        avg_ic = metrics.get('avg_ic', 0)
        ic_std = metrics.get('ic_std', 0)
        ic_consistency = metrics.get('ic_consistency', 0)
        lift = metrics.get('avg_top_decile_lift', 1.0)
        precision = metrics.get('avg_precision_at_10', 0)
        recall = metrics.get('avg_recall_at_10_for_top5', 0)
        
        lines.append(f"- **Average IC:** {avg_ic:.3f} (std: {ic_std:.3f})")
        lines.append(f"- **IC Consistency:** {ic_consistency:.0%} of folds positive")
        lines.append(f"- **Top Decile Lift:** {lift:.2f}x (top 10% returns {lift:.1f}x the average)")
        lines.append(f"- **Precision@10%:** {precision:.0%} (of top 10% predictions, {precision:.0%} are actual winners)")
        lines.append(f"- **Recall@10% for Top5%:** {recall:.0%} (captured {recall:.0%} of actual super performers)")
        lines.append("")
        lines.append("---")
        lines.append("")
        
        return lines
    
    def _generate_vol_detector_section(self, vol_corr: Dict) -> List[str]:
        """Generate volatility detector test section."""
        lines = []
        lines.append("## Volatility Detector Test")
        lines.append("")
        
        is_vol_detector = vol_corr.get('is_vol_detector', False)
        max_corr = vol_corr.get('max_vol_corr', 0)
        pred_vs_atr = vol_corr.get('pred_vs_atr', 0)
        pred_vs_natr = vol_corr.get('pred_vs_natr', 0)
        
        status = "⚠️ **WARNING: Model may be a volatility detector**" if is_vol_detector else "✅ **PASS: Not a volatility detector**"
        lines.append(status)
        lines.append("")
        lines.append(f"- Prediction vs ATR correlation: {pred_vs_atr:.3f}")
        lines.append(f"- Prediction vs nATR correlation: {pred_vs_natr:.3f}")
        lines.append(f"- Max volatility correlation: {max_corr:.3f} (threshold: 0.5)")
        lines.append("")
        
        if is_vol_detector:
            lines.append("> **Interpretation:** High correlation with volatility suggests the model")
            lines.append("> may be predicting \"high vol = high return\" rather than finding alpha.")
            lines.append("> Consider using risk-adjusted target (Option C).")
            lines.append("")
        
        lines.append("---")
        lines.append("")
        
        return lines
    
    def _generate_fold_table(self, fold_metrics: pd.DataFrame) -> List[str]:
        """Generate walk-forward results table."""
        lines = []
        lines.append("## Walk-Forward Validation Results")
        lines.append("")
        
        if fold_metrics.empty:
            lines.append("*No validation data available*")
            lines.append("")
            return lines
        
        # Build header based on available columns
        header_cols = ['Fold', 'Test Year', 'Samples', 'RMSE', 'IC', 'Edge', 'Top Decile']
        lines.append("| " + " | ".join(header_cols) + " |")
        lines.append("|" + "|".join(["------"] * len(header_cols)) + "|")
        
        for i, row in fold_metrics.iterrows():
            fold_num = i + 1
            test_year = row.get('test_year', 'N/A')
            samples = row.get('test_samples', row.get('n_test', 0))
            rmse = row.get('rmse', 0)
            ic = row.get('ic', 0)
            edge = row.get('selection_edge', 0)
            top_decile = row.get('top_decile_mean', 0)
            
            lines.append(
                f"| {fold_num} | {test_year} | {samples:,} | "
                f"{rmse:.2f}% | {ic:.3f} | {edge:+.2f}% | {top_decile:.2f}% |"
            )
        
        lines.append("")
        lines.append("---")
        lines.append("")
        
        return lines
    
    def _generate_error_analysis(self, error_analysis: Dict) -> List[str]:
        """Generate error analysis section."""
        lines = []
        lines.append("## Error Analysis")
        lines.append("")
        
        fomo = error_analysis.get('FOMO', {})
        toxic = error_analysis.get('Toxic', {})
        tp = error_analysis.get('True_Positive', {})
        
        lines.append(f"- **FOMO (Missed Winners):** {fomo.get('count', 0):,} trades, avg missed return {fomo.get('avg_missed_return', 0):+.1f}%")
        lines.append(f"- **Toxic (False Positives):** {toxic.get('count', 0):,} trades, avg loss {toxic.get('avg_loss', 0):.1f}%")
        lines.append(f"- **True Positives:** {tp.get('count', 0):,} trades, avg return {tp.get('avg_return', 0):+.1f}%")
        
        summary = error_analysis.get('summary', '')
        if summary:
            lines.append(f"- **Summary:** {summary}")
        
        lines.append("")
        lines.append("---")
        lines.append("")
        
        return lines
    
    def _generate_feature_importance(
        self, 
        feature_importance: pd.DataFrame,
        top_n: int = 20
    ) -> List[str]:
        """Generate feature importance section."""
        lines = []
        lines.append("## Feature Importance Analysis")
        lines.append("")
        lines.append(f"**Total Features:** {len(feature_importance)}")
        lines.append("")
        lines.append(f"### Top {top_n} Features by Gain")
        lines.append("")
        lines.append("| Rank | Feature | Gain | % Total | Cumulative % |")
        lines.append("|------|---------|------|---------|--------------|")
        
        # Ensure required columns exist
        if 'gain' not in feature_importance.columns:
            return lines
        
        # Sort by gain
        df = feature_importance.sort_values('gain', ascending=False).head(top_n)
        total_gain = feature_importance['gain'].sum()
        
        cumulative = 0
        for rank, (_, row) in enumerate(df.iterrows(), 1):
            gain = row['gain']
            pct = (gain / total_gain * 100) if total_gain > 0 else 0
            cumulative += pct
            feature = row.get('feature', row.name)
            
            lines.append(f"| {rank} | {feature} | {gain:.0f} | {pct:.1f}% | {cumulative:.1f}% |")
        
        lines.append("")
        lines.append("---")
        lines.append("")
        
        return lines
